// Inline status message component
export function StatusMsg({ msg, className = "" }: { msg: string; className?: string }) {
  if (!msg) return null;
  const isOk = msg.startsWith('ok:') || /sucesso|[Ss]alvo|salv[ao]s?|criado|atualizado|Perfil/i.test(msg);
  return (
    <div className={`flex items-center gap-2 text-sm font-bold px-3 py-2 rounded-xl border ${isOk ? 'text-green-400 bg-green-500/10 border-green-500/30' : 'text-red-400 bg-red-500/10 border-red-500/30'} ${className}`}>
      {isOk ? <CheckCircleIcon className="w-4 h-4 shrink-0" /> : <XCircleIcon className="w-4 h-4 shrink-0" />}
      <span>{msg.replace(/^(ok:|err:)/, '').replace(/^[✅❌]\s*/, '')}</span>
    </div>
  );
}
